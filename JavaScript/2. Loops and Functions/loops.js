
// While Loops - Pre Condition

let y = 1;

while (y < 5){
    console.log(y, "Hello World");
    y++;
}

// Do while - Post Condition
// Atleast run one time
console.log("********** Do while **********")
let x = 7;
do{
    console.log(x);
    x++;
}while(x < 5);
console.log(x);

// For Loop
console.log("********** For Loop **********")

for(let i=1;    i<5;    i++){
    console.log(i);
}