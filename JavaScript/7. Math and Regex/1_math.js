
let result = Math.ceil(4.6);     // Accpet real number and return nearest upper value
console.log(result);
console.log(Math.ceil(-4.6))

result = Math.floor(4.6);        // Accpet real number and return nearest lower value
console.log(result);
console.log(Math.floor(-4.6))

result = Math.round(4.5);        // Accpet real number and return nearest value
console.log(result);
console.log(Math.round(-4.6))

result = Math.trunc(7.6);   // Rounding to 0
console.log(result);
console.log(Math.trunc(-7.6))

